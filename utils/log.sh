# Logging utilities for consistent feedback during installation
#=======================================================================


#=======================================================================
# COLORS
#=======================================================================

NC='\033[0m' # No Color
RED='\033[0;31m'
BLUE='\033[0;34m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'


#=======================================================================
# LOGGING
#=======================================================================

log_info() {
    echo -e "${BLUE}*** INFO:${NC} $1"
}

log_success() {
    echo -e "${GREEN}*** SUCCESS:${NC} $1"
}

log_warning() {
    echo -e "${YELLOW}*** WARNING:${NC} $1"
}

log_error() {
    echo -e "${RED}*** ERROR:${NC} $1"
    exit 1
}